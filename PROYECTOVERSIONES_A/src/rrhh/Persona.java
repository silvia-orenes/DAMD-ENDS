package rrhh;

public class Persona {
	private String nombre;
	private String apellidos;
	private String dni;
	private int edad;
	
	public Persona(String nombre, String apellidos, String dni, int edad) {
		this.nombre = nombre;
		this.apellidos = apellidos;
		this.dni = dni;
		this.edad = edad;
	}
	
	
}
